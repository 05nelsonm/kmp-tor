package io.matthewnelson.kmp.tor.runtime.core.address

import io.matthewnelson.kmp.tor.runtime.core.address.IPAddress.V6.Companion.isLoopback
import io.matthewnelson.kmp.tor.runtime.core.address.IPAddress.V6.Companion.toIPAddressV6
import io.matthewnelson.kmp.tor.runtime.core.address.IPAddress.V6.Companion.toIPAddressV6OrNull
import kotlin.test.*

class IPAddressV6UnitTest {

    @Test
    fun givenBlock_whenInvalidLength_thenReturnsNull() {
        val valid = "::1000"
        assertNotNull(valid.toIPAddressV6OrNull())
        assertNull("${valid}${valid.last()}".toIPAddressV6OrNull())
    }

    @Test
    fun givenEnclosingBrackets_whenEitherOrNotBoth_thenReturnsNull() {
        val valid = "[1::1]"
        assertNotNull(valid.toIPAddressV6OrNull())
        assertNull(valid.drop(1).toIPAddressV6OrNull())
        assertNull(valid.dropLast(1).toIPAddressV6OrNull())
    }

    @Test
    fun givenIPAddressV6_whenAnyHost_thenIsInstance() {
        assertIs<IPAddress.V6.AnyHost.NoScope>("::".toIPAddressV6())
        assertIs<IPAddress.V6.AnyHost.NoScope>("::0".toIPAddressV6())

        val ah = "::%1".toIPAddressV6()
        assertIsNot<IPAddress.V6.AnyHost.NoScope>(ah)
        assertIs<IPAddress.V6.AnyHost>(ah)
    }

    @Test
    fun givenIPAddressV6_whenTypicalLoopback_thenIsInstance() {
        val notScoped = "::1".toIPAddressV6()
        assertTrue(notScoped.isLoopback())
        assertNull(notScoped.scope)

        val yesScoped = "::1%1".toIPAddressV6()
        assertTrue(yesScoped.isLoopback())
        assertNotNull(yesScoped.scope)
        assertNotEquals(notScoped, yesScoped)
    }

    @Test
    fun givenIPAddressV6_whenRandom_thenIsSuccessful() {
        for (address in TEST_ADDRESSES_IPV6.lines()) {
            address.toIPAddressV6()
        }
    }

    @Test
    fun givenIPAddressV6_whenCanonicalized_thenReturnsBracketed() {
        val rawAddress = "35f4:c60a:8296:4c90:79ad:3939:69d9:ba10"
        val address = rawAddress.toIPAddressV6()

        val expected = "[$rawAddress]"
        val actual = address.canonicalHostName()
        assertEquals(expected, actual)
    }

    @Test
    fun givenIPAddressV6_whenBracketed_thenDropsFirstAndLast() {
        val expected = "35f4:c60a:8296:4c90:79ad:3939:69d9:ba10"
        val actual = "[$expected]".toIPAddressV6().value
        assertEquals(expected, actual)
    }

    @Test
    fun givenIPAddressV6Url_whenFromString_thenReturnsSuccess() {
        val expected = "35f4:c60a:8296:4c90:79ad:3939:69d9:ba10".toIPAddressV6()
        val actual = "https://username:password@${expected.canonicalHostName()}:9822/some/path".toIPAddressV6()
        assertEquals(expected, actual)
    }

